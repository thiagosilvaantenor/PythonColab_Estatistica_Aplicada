# CRIANDO FUNÇÃO PROBABILIDADE
def probab (A, E):
  # A = obj de estudo, E = espaço amostral
  resultado = (A / E)*100
  print('{:.2f}'.format(resultado))

# PROBABILIDADE DE RETIRAR PREFÊRENCIA COR PRETA
probab (len(cor_preta), len(dados))

