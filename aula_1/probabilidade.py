"""## **PROBABILIDADE** **DE NÃO OCORRER**"""

# CRIANDO FUNÇÃO PROBABILIDADE DE NÃO OCORRER UM EVENTO
def probab_nao (A, E):
  resultado = (1- (A/E))*100
  print('{:.2f}'.format(resultado))

cor_vermelho = dados.loc[dados.cor_favorita.isin(['vermelha', 'vermelho'])]
